app.config(['$routeProvider', function($routeProvider) {
    $routeProvider.when('/login', {
        controller: 'LoginController',
        templateUrl: 'partials/login.html'
    }).when('/home', {
        templateUrl: 'partials/home.html'
    }).when('/kelas', {
        templateUrl: 'partials/kelas.html',
        controller: 'KelasController'
    }).when('/kelas/edit/:id', {
        templateUrl: 'partials/kelas-form.html',
        controller: 'KelasEditController'
    }).when('/member', {
        templateUrl: 'partials/member.html',
        controller: 'MemberListController'
    }).when('/member/tambah', {
        templateUrl: 'partials/member-form.html',
        controller: 'MemberTambahController'
    }).when('/member/edit/:id', {
        templateUrl: 'partials/member-form.html',
        controller: 'MemberEditController'
    }).when('/member/perpanjang/:id', {
        templateUrl: 'partials/perpanjang-form.html',
        controller: 'PerpanjangController'
    }).when('/perpanjang-member', {
        templateUrl: 'partials/perpanjang.html',
        controller: 'PerpanjangListController'
    }).when('/about', {
        templateUrl: 'partials/about.html'
    }).when('/contact', {
        templateUrl: 'partials/contact.html'
    }).when('/barang', {
        templateUrl: 'partials/barang.html',
        controller: 'BarangListController'
    }).when('/barang/tambah', {
        templateUrl: 'partials/barang-form.html',
        controller: 'BarangTambahController'
    }).when('/barang/edit/:id', {
        templateUrl: 'partials/barang-form.html',
        controller: 'BarangEditController'
    }).when('/non-member', {
        templateUrl: 'partials/non-member.html',
        controller: 'NonMemberListController'
    }).when('/non-member/tambah', {
        templateUrl: 'partials/non-member-form.html',
        controller: 'NonMemberTambahController'
    }).when('/non-member/edit/:id', {
        templateUrl: 'partials/non-member-form.html',
        controller: 'NonMemberEditController'
    }).when('/pembelian', {
        templateUrl: 'partials/pembelian.html',
        controller: 'PembelianListController'
    }).when('/pembelian/tambah', {
        templateUrl: 'partials/pembelian-form.html',
        controller: 'PembelianTambahController'
    }).when('/penjualan', {
        templateUrl: 'partials/penjualan.html',
        controller: 'PenjualanListController'
    }).when('/penjualan/tambah', {
        templateUrl: 'partials/penjualan-form.html',
        controller: 'PenjualanTambahController'
    }).when('/rekap-member', {
        templateUrl: 'partials/rekap-member.html',
        controller: 'RekapMemberController'
    }).when('/rekap-non-member', {
        templateUrl: 'partials/rekap-non-member.html',
        controller: 'RekapNonMemberController'
    }).when('/rekap-penjualan', {
        templateUrl: 'partials/rekap-penjualan.html',
        controller: 'RekapPenjualanController'
    }).when('/rekap-barang', {
        templateUrl: 'partials/rekap-barang.html',
        controller: 'RekapBarangController'
    }).when('/rekap-perpanjang', {
        templateUrl: 'partials/rekap-perpanjang.html',
        controller: 'RekapPerpanjangController'
    }).otherwise({
        redirectTo: '/login'
    });
}]);